package o218;
public class Test03_bit {
	public static void main(String[] args) {
	// 비트합(OR) : 하나만 1이어도 1
	System.out.println(5|3); // 7   0101 | 0011 = 0111(7)
	
	// 비트곱(AND) : 둘 다 1이어야 1
	System.out.println(5&3); // 1  0101 & 0011 = 0001(1)
		
	}
}
